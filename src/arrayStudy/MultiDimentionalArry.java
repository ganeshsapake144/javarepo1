package arrayStudy;

public class MultiDimentionalArry {

	public static void main(String[] args)
	{
		  String animal[][]=new String[2][2];
		  
		  
		  
		  animal[0][0]="Dog";
		  animal[0][1]="Cat";
		  animal[1][0]="Tiger";
		  animal[1][1]="Snack";
		  
		  
		  System.out.print(animal[0][0]+" ");
		  System.out.println(animal[0][1]+" ");
		  System.out.print(animal[1][0]+" ");
		  System.out.println(animal[1][1]+" ");
		  

	}

}
