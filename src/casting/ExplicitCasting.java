package casting;

public class ExplicitCasting {

	public static void main(String[] args) 
	{
	// explicit casting/ narrowing casting
	 //  higher-->lower
	//   memory size-->goes on decreasing 
	//   data loss-->may be data loss

		double a= 10.1234;
		
		System.out.println("a");
		
		int b=(int)a;
		
		System.out.println("b");
		
		
	}

}
