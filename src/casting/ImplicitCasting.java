package casting;

public class ImplicitCasting {

	public static void main(String[] args)
	{
		//implicit casting/ widening casting
	//	lower-->higher
	//	memory size --> goas on increasing
	//	data loss-->no data loss

		int a=10;
		
		System.out.println("a");
		
		
		double b=a;
		
		System.out.println("b");
		
		
	}

}
